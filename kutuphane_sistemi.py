# --- SOMUT SINIFLAR (İŞLEMLER) ---

class KitapIslem(Islem):
    kitap_sayisi = 0 

    def __init__(self):
        self._kitaplar = []

    def ekle(self, kitap):

        for k in self._kitaplar:
            if k.kayit_no == kitap.kayit_no:
                print("Hata: Bu kayıt numarası ile zaten bir kitap mevcut!")
                return

        self._kitaplar.append(kitap)
        KitapIslem.kitap_sayisi += 1
        print("\nKitap başarıyla eklendi.")
        print(f"Toplam Kitap Sayısı: {KitapIslem.kitap_sayisi}")

    def sil(self, kayit_no):
        for kitap in self._kitaplar:
            if kitap.kayit_no == kayit_no:
                self._kitaplar.remove(kitap)
                KitapIslem.kitap_sayisi -= 1
                print("Kitap başarıyla silindi.")
                return
        print("Hata: Belirtilen kayıt numarasına sahip kitap bulunamadı.")

    def guncelle(self):
        while True:
            kayit_no = input("Güncellemek istediğiniz kitabın kayıt numarasını girin (iptal için 'iptal' yazın): ")
            if kayit_no.lower() == 'iptal':
                print("Kitap güncelleme işlemi iptal edildi.")
                return

            found_kitap = None
            for kitap in self._kitaplar:
                if kitap.kayit_no == kayit_no:
                    found_kitap = kitap
                    break

            if found_kitap:
                print("Yeni bilgileri giriniz (Boş bırakılan alanlar değiştirilmez):")
                yeni_baslik = input(f"Yeni Başlık (Mevcut: {found_kitap.baslik}): ")
                yeni_yazar = input(f"Yeni Yazar (Mevcut: {found_kitap.yazar}): ")
                yeni_sayfa = input(f"Yeni Sayfa Sayısı (Mevcut: {found_kitap.sayfa_sayisi}): ")

                if yeni_baslik: found_kitap.baslik = yeni_baslik
                if yeni_yazar: found_kitap.yazar = yeni_yazar
                if yeni_sayfa: found_kitap.sayfa_sayisi = yeni_sayfa

                print("Kitap başarıyla güncellendi.")
                return
            else:
                print("Hata: Belirtilen kayıt numarasına sahip kitap bulunamadı. Lütfen tekrar deneyin veya 'iptal' yazın.")

    def listele(self):
        
        if not self._kitaplar:
            print("Kayıt bulunamadı.")
            return
        print("\n--- Kitap Listesi ---")
        for kitap in self._kitaplar:
            print(kitap)


