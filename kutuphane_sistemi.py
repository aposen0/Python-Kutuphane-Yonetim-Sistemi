from abc import ABC, abstractmethod

# --- SOYUT SINIFLAR ---

class Kaynak(ABC):
    def __init__(self, baslik, kayit_no):
        self._baslik = baslik
        self._kayit_no = kayit_no

    @property
    def baslik(self):
        return self._baslik

    @baslik.setter
    def baslik(self, value):
        self._baslik = value

    @property
    def kayit_no(self):
        return self._kayit_no

    @kayit_no.setter
    def kayit_no(self, value):
        self._kayit_no = value

    @abstractmethod
    def __str__(self):
        pass


class Islem(ABC):
    @abstractmethod
    def ekle(self, item):
        pass

    @abstractmethod
    def sil(self, kayit_no):
        pass

    @abstractmethod
    def guncelle(self):
        pass

    @abstractmethod
    def listele(self):
        pass


# --- SOMUT SINIFLAR (KAYNAKLAR) ---

class Kitap(Kaynak):
    def __init__(self, baslik, kayit_no, yazar, sayfa_sayisi):
        super().__init__(baslik, kayit_no)
        self._yazar = yazar
        self._sayfa_sayisi = sayfa_sayisi

    @property
    def yazar(self):
        return self._yazar

    @yazar.setter
    def yazar(self, value):
        self._yazar = value

    @property
    def sayfa_sayisi(self):
        return self._sayfa_sayisi

    @sayfa_sayisi.setter
    def sayfa_sayisi(self, value):
        self._sayfa_sayisi = value

    def __str__(self):
        return f"Kitap: {self.baslik} | Kayıt No: {self.kayit_no} | Yazar: {self.yazar} | Sayfa: {self.sayfa_sayisi}"


class Dergi(Kaynak):
    def __init__(self, baslik, kayit_no, yayin_donemi, sayi_no):
        super().__init__(baslik, kayit_no)
        self._yayin_donemi = yayin_donemi
